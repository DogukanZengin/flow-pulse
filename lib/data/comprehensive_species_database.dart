    final counts = <BiomeType, int>{};
    for (final biome in BiomeType.values) {
      counts[biome] = getSpeciesForBiome(biome).length;
    }
    return counts;
  }

  /// Get species count by rarity
  static Map<CreatureRarity, int> getSpeciesCountByRarity() {
    final counts = <CreatureRarity, int>{};
    for (final rarity in CreatureRarity.values) {
      counts[rarity] = getSpeciesByRarity(rarity).length;
    }
    return counts;
  }

  /// Get total species count
  static int getTotalSpeciesCount() => allSpecies.length;

  /// Validate database completeness (should be 144 species total)
  static bool validateDatabase() {
    final total = getTotalSpeciesCount();
    final byBiome = getSpeciesCountByBiome();
    
    // Check total count
    if (total != 144) return false;
    
    // Check each biome has exactly 36 species
    for (final count in byBiome.values) {
      if (count != 36) return false;
    }
    
    // Check rarity distribution per biome (should be: 25 common, 7 uncommon, 3 rare, 1 legendary)
    for (final biome in BiomeType.values) {
      final biomeSpecies = getSpeciesForBiome(biome);
      final commonCount = biomeSpecies.where((s) => s.rarity == CreatureRarity.common).length;
      final uncommonCount = biomeSpecies.where((s) => s.rarity == CreatureRarity.uncommon).length;
      final rareCount = biomeSpecies.where((s) => s.rarity == CreatureRarity.rare).length;
      final legendaryCount = biomeSpecies.where((s) => s.rarity == CreatureRarity.legendary).length;
      
      if (commonCount != 25 || uncommonCount != 7 || rareCount != 3 || legendaryCount != 1) {
        return false;
      }
    }
    
    return true;
  }
